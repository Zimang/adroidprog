package top.zimang.listviewtest.model;

public class Fairy {
    private String name;
    private int imageId;

    public Fairy(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
